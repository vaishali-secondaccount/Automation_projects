package Control_statement_practice;

public class Switch_practice {

	public static void main(String[] args) {
		int val=1;
		switch(val) {
		
		case 1:System.out.println("Tea");
		break;
		case 2:System.out.println("Coffee");
		break;
		case 3: System.out.println("Juice");
		break;
		default: System.out.println("Invalid input enter valid input for tea coffee or juice");
		
		
		}
	}

}
